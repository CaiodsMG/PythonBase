numero1 = 10
numero2 = 5

adicao = numero1 + numero2
print('Adição:', adicao) #Retorna a adição

subtracao = numero1 - numero2
print('Subtração', subtracao) #Retorna a subtração

multiplicacao = numero1 * numero2
print('Multiplicação:', multiplicacao) #Retorna a multiplicação

divisao = numero1 / numero2
print('Divisão', divisao) #Retorna a divisão

divisao_inteira = numero1 // numero2
print('Divisão Inteira:', divisao_inteira) #Retorna a divisão inteira

exponenciacao = numero1 ** numero2
print('Exponenciação:', exponenciacao) #Retorna a exponenciação

modulo = numero1 % numero2
print('Módulo:', modulo) #Retorna o resto da divisão