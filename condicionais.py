entrada = input('Você quer entrar ou sair? ')
if entrada == 'entrar':
    print('Você entrou no sistema') # Resposta apresentada no terminal caso a entrada seja "entrar"
elif entrada == 'sair':
    print('Você saiu do sistema') # Resposta apresentada no terminal caso a entrada seja "sair"
else:
    print('Você não digitou nem entrar nem sair') # Resposta apresentada no terminal caso a entrada não seja "entrar" ou "sair"


